import math # Importacion del modulo math

# Round
print( round( 1.3 ) ) #Nos retorna el numero mas cercado
print( round( 1.7 ) ) #Nos retorna el numero mas cercado
print( round( 1.5 ) ) #Nos retorna el numero mas cercado

# abs
print( abs( -100 ) ) #Nos retorna el numero absoluto que le pacemos a la funcion.
print( abs( 100 ) ) #Nos retorna el numero absoluto que le pacemos a la funcion.

# Como python no trae muchas funciones de este estilo, nosotros podemos usar un modulo llamado "Modulo math"
# Â¿ Que es un modulo ?
# Un modulo es como un archivo que viene de forma nativa en python y que podemos usar.

# Metodos los modulo math.
print( math.ceil( 1.5 ) ) #Toma un numero y lo lleva al numero maximo mas certano
print( math.floor( 1.99999999 ) ) #Toma un numero y lo lleva al numero minimo mas cercano
print( math.isnan( 23 ) ) #Toma un numero y comprueba si no es un numero
print( math.pow( 10, 3 ) ) #Toma un numero y lleva a la potencia de algo
print( math.sqrt( 10, 3 ) ) #Toma un numero y saca la raiz cuadrada

