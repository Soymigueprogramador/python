# Â¿ Que es una expresion ?
###
# Una expresion es una cadena de instrucciones que se termina evaluando en algo.
# Ejemplo: (1 + 2 = 3) <= esto es una expresion
###

# Ejemplos en codigo. Debemos recordar que en estos casos solo devolvera true o false 
print( 1 < 2 ) #Esto devuelve true.
print( 1 > 2 ) #Esto devuelve false.
print( 1 <= 2 ) #Esto devuelve true.
print( 1 >= 2 ) #Esto devuelve false.
print( 2 == 2 ) #Esto devuelve true.
print( 2 != 2 ) #Esto devuelve false.

