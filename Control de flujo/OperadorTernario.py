# Operadores ternarios.

edad = 21

# Codigo de ejemple:
mensaje = "Es mayor" if edad > 11 else "Es menor"

print(mensaje)

# El operador ternario tiene como objetivo tomar un valor y asignarselo a una variable.

# Sintaxis de un operador ternario en python. 
###
# Creamos una variable a la izquierda.
# Le agregamos un valor.
# Tenemos la condicion.
# El otro valor.
###

# La condicion tiene que estar rodeada por un valor a la derecha y a la izquierda
# En caso de que la condicion sea falsa se asigna el valor de la derecha.
