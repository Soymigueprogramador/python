# Cadena de comparadores

# Con una cadena de comparadores podemos escribir el mismo código de forma más corta y clara.

# Sin cadena de comparadores:
edad = 20

if edad >= 20 and edad <= 60:
    print("Podés pasar")
else:
    print("No podés pasar.")

# Con cadena de comparadores:
if 20 <= edad <= 60:
    print("Podés pasar")
else:
    print("No podés pasar.")
