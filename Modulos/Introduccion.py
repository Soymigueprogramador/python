# Introduccion a modulos en Python

###
# Hasta ahora creabamos las funciones, variables dentro de un unico archivo pero ahora con los
# modulos lo que vamos a poder hacer es crear los bloques de codigo en distintos archivos y
# luego lo vamos a poder llamar y utilizar en un archivo diferente.
###

# Los ejemplos del codigo estan dentro de la carpeta "Ejemplos"

# Aclaraciones:
###
# Nunca se debe importar todo el archivo completo, tenemos que indicarle a python lo que vamos a usar.
# Por comodidad podemos importar varias funciones separadas por una ,
# Si tenemos un archivo con 2 palabras se lo tiene que nombre con un _ palabra_secreta
# El orden de importacion es importante
# Si importamos con un * primero hacemos la importacion y despues el llamado
# Python crea automaticamente una  llamada "__pycache__" donde guarda los archivos importados
###
